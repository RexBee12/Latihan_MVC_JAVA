    private void setupForm() {
        jComboBoxProdi.removeAllItems();
        for (String prodi : PRODI_LIST) {
            jComboBoxProdi.addItem(prodi);
        }
        
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setColumnIdentifiers(new Object[]{"Nama", "NIM", "NIK", "Program Studi"});

        // Klik baris tabel → isi form
        jTable2.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent e) {
                onTableClick();
            }
        });
        
        loadData();
    }
    
    private void loadData() {
        allData = controller.getStudent();
        currentPage = 1;
        renderTable();
    }
    
    private void renderTable() {
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0);

        if (allData == null || allData.isEmpty()) {
            jButton5.setEnabled(false);
            jButton6.setEnabled(false);
            return;
        }
        
        int start = (currentPage - 1) * PAGE_SIZE;
        int end   = Math.min(start + PAGE_SIZE, allData.size());
        
        for (int i = start; i < end; i++) {
            Student s = allData.get(i);
            model.addRow(new Object[]{
                s.getName(),
                s.getNim(),
                s.getNik(),
                s.getStudyProgram()
        });
    }

    jButton5.setEnabled(currentPage > 1);
    jButton6.setEnabled(end < allData.size());

    }
    
    private void onTableClick() {
        int row = jTable2.getSelectedRow();
        if (row < 0) return;
        
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        jTextFieldNama.setText(model.getValueAt(row, 0).toString());
        jTextFieldNIM.setText(model.getValueAt(row, 1).toString());
        jTextFieldNIK.setText(model.getValueAt(row, 2).toString());
        jComboBoxProdi.setSelectedItem(model.getValueAt(row, 3).toString());
        selectedNim = model.getValueAt(row, 1).toString();
    }

    private void clearForm() {
        jTextFieldNama.setText("");
        jTextFieldNIM.setText("");
        jTextFieldNIK.setText("");
        jComboBoxProdi.setSelectedIndex(0);
        selectedNim = "";
    }
    
    private boolean isFormValid() {
        if (jTextFieldNama.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Nama tidak boleh kosong!");
            return false;
        }
        if (jTextFieldNIM.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "NIM tidak boleh kosong!");
            return false;
        }
        if (jTextFieldNIK.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "NIK tidak boleh kosong!");
            return false;
        }
        if (jComboBoxProdi.getSelectedIndex() == 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih Program Studi!");
            return false;
        }
        return true;
    }
    
