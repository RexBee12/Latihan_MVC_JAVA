    private void setupForm() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setColumnIdentifiers(new Object[]{"ID", "Nama", "NIK", "NIDN", "Keahlian"});

        jTable1.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent e) {
                onTableClick();
            }
        });

        loadData();
    }

    private void loadData() {
        allData = controller.getLecturer();
        currentPage = 1;
        renderTable();
    }

    private void renderTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        int start = (currentPage - 1) * PAGE_SIZE;
        int end   = Math.min(start + PAGE_SIZE, allData.size());

        for (int i = start; i < end; i++) {
            Lecturer d = allData.get(i);
            model.addRow(new Object[]{
                d.getId(),
                d.getName(),
                d.getNik(),
                d.getNidn(),
                d.getKeahlian()
            });
        }

        jButton5.setEnabled(currentPage > 1);
        jButton6.setEnabled(end < allData.size());
    }

    private void onTableClick() {
        int row = jTable1.getSelectedRow();
        if (row < 0) return;

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        jTextFieldNama.setText(model.getValueAt(row, 1).toString());
        jTextFieldNIK.setText(model.getValueAt(row, 2).toString());
        jTextFieldNIDN.setText(model.getValueAt(row, 3).toString());
        jTextFieldKeahlian.setText(model.getValueAt(row, 4).toString());
        selectedNidn = model.getValueAt(row, 3).toString();
    }

    private void clearForm() {
        jTextFieldNama.setText("");
        jTextFieldNIK.setText("");
        jTextFieldNIDN.setText("");
        jTextFieldKeahlian.setText("");
        selectedNidn = "";
    }

    private boolean isFormValid() {
        if (jTextFieldNama.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Nama tidak boleh kosong!");
            return false;
        }
        if (jTextFieldNIK.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "NIK tidak boleh kosong!");
            return false;
        }
        if (jTextFieldNIDN.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "NIDN tidak boleh kosong!");
            return false;
        }
        if (jTextFieldKeahlian.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Keahlian tidak boleh kosong!");
            return false;
        }
        return true;
    }

