    private void setupForm() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setColumnIdentifiers(new Object[]{"ID", "Kode", "Nama", "SKS", "Semester"});

        jTable1.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent e) {
                onTableClick();
            }
        });

        loadData();
    }
    
    private void loadData() {
        allData = controller.getCourse();
        currentPage = 1;
        renderTable();
    }
    
    private void renderTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        int start = (currentPage - 1) * PAGE_SIZE;
        int end   = Math.min(start + PAGE_SIZE, allData.size());

        for (int i = start; i < end; i++) {
            Course mk = allData.get(i);
            model.addRow(new Object[]{
                mk.getId(),
                mk.getKode(),
                mk.getNamaMk(),
                mk.getSks(),
                mk.getSemester()
            });
        }

        jButton5.setEnabled(currentPage > 1);
        jButton6.setEnabled(end < allData.size());
    }
    
    private void onTableClick() {
        int row = jTable1.getSelectedRow();
        if (row < 0) return;

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        jTextFieldKode.setText(model.getValueAt(row, 1).toString());
        jTextFieldNama.setText(model.getValueAt(row, 2).toString());
        jTextFieldSKS.setText(model.getValueAt(row, 3).toString());
        jTextFieldSemester.setText(model.getValueAt(row, 4).toString());
        selectedKode = model.getValueAt(row, 1).toString();
    }
    
    private void clearForm() {
        jTextFieldKode.setText("");
        jTextFieldNama.setText("");
        jTextFieldSKS.setText("");
        jTextFieldSemester.setText("");
        selectedKode = "";
    }
    
    private boolean isFormValid() {
        if (jTextFieldKode.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Kode tidak boleh kosong!");
            return false;
        }
        if (jTextFieldNama.getText().trim().isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Nama tidak boleh kosong!");
            return false;
        }
        try {
            Integer.parseInt(jTextFieldSKS.getText().trim());
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "SKS harus berupa angka!");
            return false;
        }
        try {
            Integer.parseInt(jTextFieldSemester.getText().trim());
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Semester harus berupa angka!");
            return false;
        }
        return true;
    }

