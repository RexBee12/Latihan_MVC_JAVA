    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        String keyword = jTextField1.getText().trim();
        if (keyword.isEmpty() || keyword.equals("Pencarian")) {
            loadData();
            return;
        }
        allData = controller.search(keyword);
        currentPage = 1;
        renderTable();
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (!isFormValid()) return;

        Course mk = new Course(
            jTextFieldKode.getText().trim(),
            jTextFieldNama.getText().trim(),
            Integer.parseInt(jTextFieldSKS.getText().trim()),
            Integer.parseInt(jTextFieldSemester.getText().trim())
        );

        if (selectedKode.isEmpty()) {
            int result = controller.create(mk);
            if (result > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil disimpan!");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Gagal menyimpan data!");
            }
        } else {
            int result = controller.update(mk, selectedKode);
            if (result > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil diupdate!");
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Gagal mengupdate data!");
            }
        }

        clearForm();
        loadData();
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (selectedKode.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus!");
            return;
        }

        int confirm = javax.swing.JOptionPane.showConfirmDialog(
            this, "Yakin ingin menghapus data ini?", "Konfirmasi",
            javax.swing.JOptionPane.YES_NO_OPTION
        );

        if (confirm == javax.swing.JOptionPane.YES_OPTION) {
            try {
                int result = controller.delete(selectedKode);
                if (result > 0) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                    clearForm();
                    loadData();
                }
            } catch (Exception e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Gagal menghapus: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

